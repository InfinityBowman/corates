/**
 * Escape HTML to prevent XSS in email content and other HTML contexts
 * Escapes: &, <, >, ", '
 */
export function escapeHtml(text) {
  if (text == null) return '';
  const map = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;',
  };
  return String(text).replace(/[&<>"']/g, m => map[m]);
}
